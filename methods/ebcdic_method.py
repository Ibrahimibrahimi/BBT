class EBCDICMethod(BaseMethod):
    name = "EBCDIC"
    description = "EBCDIC encoding"
    category = "Encoding"

    def encode(self, text: str) -> str:
        result = []
        for char in text:
            cp = ord(char)
            ebcdic_byte = ASCII_TO_EBCDIC.get(cp, cp)
            result.append(f"{ebcdic_byte:02x}")
        return " ".join(result)
